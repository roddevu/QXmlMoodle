package fr.qxmlmoodle.warning;

/** Enumeration ImportWarningType enumerate the different type of warning. */
public enum ImportWarningType {
    /** Warning concern unknown element. */
    UNKNOWN_ELEMENT,
    /** Warning concern unknown value. */
    UNKNOWN_VALUE,
    /** Warning concern bad value. */
    BAD_VALUE,
    /** Warning concern missing. */
    MISSING,
    /** Warning concern other. */
    OTHER
}
