package fr.qxmlmoodle.question.text;

/** Enumeration QuestionTextFormat enumerate all kind of question format. */
public enum QuestionTextFormat {
    /** (default format). */
    HTML,
    /** Moodle auto format. */
    MOODLE_AUTO_FORMAT,
    /** Plain Text format. */
    PLAIN_TEXT,
    /** Markdown format. */
    MARKDOWN
}
